package com.example.model;

public class ActivityCalories {
    private String activityType;
    private float caloriesPerMinute;

    // Getters and Setters
    public String getActivityType() { return activityType; }
    public void setActivityType(String activityType) { this.activityType = activityType; }
    public float getCaloriesPerMinute() { return caloriesPerMinute; }
    public void setCaloriesPerMinute(float caloriesPerMinute) { this.caloriesPerMinute = caloriesPerMinute; }
}
